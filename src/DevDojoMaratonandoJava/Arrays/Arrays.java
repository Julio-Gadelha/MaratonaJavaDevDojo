package DevDojoMaratonandoJava.Arrays;

public class Arrays {
   public static void main(String[] args) {
       String [] nomes= new String[4];
       nomes[0] = "Goku";
       nomes[1] = "Picollo";
       nomes[2] = "Vegetta";
       nomes[3] = "Pateta";
       for (int i = 0; i < nomes.length; i++) {
           System.out.println(nomes[i]);
       }
    }
}
